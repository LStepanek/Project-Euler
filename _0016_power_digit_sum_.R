# 2^15 = 32768 and the sum of its digits is 3 + 2 + 7 + 6 + 8 = 26.

# What is the sum of the digits of the number 2^1000?


## -----------------------------------------------------------------------------

################################################################################

## my solution using R ---------------------------------------------------------

n <- 1000

my_power <- c(1)

for(i in 1:n){
    
    temp_power <- NULL
    my_overdigit <- 0
    
    for(j in length(my_power):1){
        
        temp_power <- c(
            
            (my_power[j] * 2 + my_overdigit) %% 10,
            temp_power
            
        )
        
        my_overdigit <- (
            
            my_power[j] * 2 + my_overdigit
            
        ) %/% 10
        
        
    }
    
    if(
        my_overdigit > 0
    ){
        
        temp_power <- c(
            
            my_overdigit,
            temp_power
            
        )
        
    }
    
    my_power <- temp_power
    
}


## -----------------------------------------------------------------------------

print(
    sum(my_power)
)   # 1366


